def get_image_prompt(genre_id: str | None) -> str | None:
    """장르 id → '공통 프리픽스 + 풀 랜덤 1개 + 장르별 분위기 꼬리표'. 미매칭/빈 풀이면 None(폴백)."""
    gid = (genre_id or "").strip().lower()
    pool = IMAGE_PROMPTS.get(gid)
    if not pool:
        return None
    suffix = BRIGHT_SUFFIX if gid in BRIGHT_GENRES else GENRE_SUFFIX.get(gid, "")
    return f"{IMAGE_PROMPT_PREFIX} {random.choice(pool)} {suffix}".rstrip()
